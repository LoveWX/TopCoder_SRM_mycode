class GoldenChain {
public:
	int minCuts(vector <int>);
};

int GoldenChain::minCuts(vector <int> sections)
{
	int i=0,j=sections.size()-1,space=sections.size(),ans=0;
	sort(sections.begin(),sections.end());
	while(sections[i]<=space-1)
	{
		if(sections[i]==space-1)
		{
			return ans+sections[i];
		}
		ans+=sections[i];
		j--;
		sections[j]+=sections[j+1];
		space=space-1-sections[i];
		i++;
	}
	if(sections[i]==space)
	{
		ans+=sections[i];
		return ans;
	}
	return ans+space;
}


