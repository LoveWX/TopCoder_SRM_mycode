class FormatAmt {
public:
	string amount(int, int);
};

string FormatAmt::amount(int dollars, int cents)
{
	int i,j,top=0;
	char yuan[50];
	while(1)
	{
		if(dollars==0)
			break;
		yuan[top++]=dollars%10+'0';
		dollars/=10;
		if(dollars==0)
			break;
		yuan[top++]=dollars%10+'0';
		dollars/=10;
		if(dollars==0)
			break;
		yuan[top++]=dollars%10+'0';
		dollars/=10;
		if(dollars==0)
			break;
		yuan[top++]=',';
	}
	if(top==0)
	{
		yuan[0]='0';
		top++;
	}
	yuan[top]='\0';
	i=0;
	j=top-1;
	while(i<j)
	{
		char tch=yuan[i];
		yuan[i]=yuan[j];
		yuan[j]=tch;
		i++;
		j--;
	}
	string ans="$";
	ans+=string(yuan);
	sprintf(yuan,".%02d",cents);
	ans+=string(yuan);
	return ans;
}


